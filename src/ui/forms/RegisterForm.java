    private void RegisterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegisterButtonActionPerformed
        Integer pib = 0;
        try {
            pib = PibTextField.getText().equals("") ? 0 : Integer.parseInt(PibTextField.getText());
        } catch (NumberFormatException e) {
            ErrorMessageLabel.setText("PIB mora biti 9 cifara");
            return;
        }
        String naziv = NazivTextFlied.getText().strip();
        String email = EmailTextFlied.getText().strip();
        String brojRacuna = RacunlTextFlied.getText().strip();
        String lozinka = String.copyValueOf(jPasswordField1.getPassword());
        String ponovnaLozinka = String.copyValueOf(jPasswordField2.getPassword());
        if (validirajUnos(pib, naziv, email, brojRacuna, lozinka, ponovnaLozinka)) {
            Dobavljac novi = new Dobavljac(pib, naziv, email, lozinka, brojRacuna);
            if (Controller.getInstance().registerDobavljac(novi)) {
                this.dispose();
                return;
            }
            JOptionPane.showMessageDialog(this, "Greska pri registraciji pokusajte ponovo.", "GRESKA", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_RegisterButtonActionPerformed

