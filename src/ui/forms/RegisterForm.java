    private boolean validirajUnos(Integer pib, String naziv, String email, String brojRacuna, String lozinka, String ponovnaLozinka) {
        if ( (pib+"").length() != 9) {
            ErrorMessageLabel.setText("PIB mora biti 9 cifara");
            return false;
        }
        if (naziv==null || naziv.length() < 2) {
            ErrorMessageLabel.setText("Naziv mora biti bar 2 karaktera");
            return false;
        }
        if (email==null || !email.contains("@") || !email.endsWith(".com")) {
            ErrorMessageLabel.setText("Email mora biti validan");
            return false;
        }
        if (brojRacuna==null || brojRacuna.length()<9 || brojRacuna.charAt(3)!='-' || brojRacuna.charAt(brojRacuna.length()-4)!='-')  {
            ErrorMessageLabel.setText("Broj racuna mora biti validan");
            return false;
        }
        if (lozinka==null || lozinka.length() < 8) {
            ErrorMessageLabel.setText("Lozinka mora da ima bar 8 karaktera");
            return false;
        }
        if (ponovnaLozinka==null || !ponovnaLozinka.equals(lozinka)) {
            ErrorMessageLabel.setText("Loiznke se ne poklapaju");
            return false;
        }
        ErrorMessageLabel.setText("   ");
        return true;
    }
